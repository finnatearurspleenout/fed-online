function sum(a, b) {
    let result = a + b;
    console.log(`${a} + ${b} = ${result}`);
}

function minus(a, b) {
    let result = a - b;
    console.log(`${a} - ${b} = ${result}`);
}

function multi(a, b) {
    let result = a * b;
    console.log(`${a} * ${b} = ${result}`);
}

function division(a, b) {
    let result = a / b;
    console.log(`${a} / ${b} = ${result}`);
}

export {sum, minus, multi, division};